package com.verizon.bean;

public class LoginUser {
	private String strUserName = "", strFirstName = "", strMiddleName = "", strLastName = "", strPhonenumber = "";

	

	public String getStrUserName() {
		return strUserName;
	}

	public void setStrUserName(String strUserName) {
		this.strUserName = strUserName;
	}

	public String getStrFirstName() {
		return strFirstName;
	}

	public void setStrFirstName(String strFirstName) {
		this.strFirstName = strFirstName;
	}

	public String getStrMiddleName() {
		return strMiddleName;
	}

	public void setStrMiddleName(String strMiddleName) {
		this.strMiddleName = strMiddleName;
	}

	public String getStrLastName() {
		return strLastName;
	}

	public void setStrLastName(String strLastName) {
		this.strLastName = strLastName;
	}

	public String getStrPhonenumber() {
		return strPhonenumber;
	}

	public void setStrPhonenumber(String strPhonenumber) {
		this.strPhonenumber = strPhonenumber;
	}
	
	
}
